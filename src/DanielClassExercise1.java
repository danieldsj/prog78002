
/**
 * Declare two variables that will hold two parts of the expression provided
 * in module 4, and perform the division within a new System.out.println 
 * statement.  The results of the output should be the same.
 *
 * @author Daniel de Sao Jose (991500928)
 */
public class DanielClassExercise1 {
    public static void main(String[] args) {
        
        // The original print statement.
        System.out.println((10.5 + 2 * 3) / (45 - 3.5));
        
        // The two variables containing the two parts of the expression.
        double numerator = 10.5 + 2 * 3;
        double denominator = 45 - 3.5;
        
        // The new System.out.println statement that performs division.
        System.out.println(numerator / denominator);
        
    }

}
